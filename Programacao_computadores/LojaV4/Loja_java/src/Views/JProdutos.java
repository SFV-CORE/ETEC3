    private void btnAtivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtivoActionPerformed
        // TODO add your handling code here:
        //Verifica se esta vazio
        if (txtCodProd.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor preencha todos os campos!");
        } else {
            //variaveis
            int cod = Integer.valueOf(txtCodProd.getText());
            //---------
            if ("Desativar".equals(btnAtivo.getText())) {
                try {
                    //seta os dados e executa a desativação
                    mod.set_codProd(cod);
                    mod.setAtivo(0);
                    int r = dao.mudarAtivo(mod);
                    //verifica se deu certo
                    if (r == 1) {
                        //se deu certo, atualiza a tabela clientes e carrega a config inicial
                        tInicial();
                        dao.mostrarTodos((DefaultTableModel) jtProdutos.getModel());
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Erro: " + e);//exibe o erro
                }
            } else {
                try {
                    //seta os dados e executa a ativação
                    mod.set_codProd(cod);
                    mod.setAtivo(1);
                    int r = dao.mudarAtivo(mod);
                    //verifica se deu certo
                    if (r == 1) {
                        //se deu certo, atualiza a tabela clientes e carrega a config inicial
                        tInicial();
                        dao.mostrarTodos((DefaultTableModel) jtProdutos.getModel());
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Erro: " + e);//exibe o erro
                }
            }

        }
    }//GEN-LAST:event_btnAtivoActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
        //verifica se esta vazio
        if (txtCodProd.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor preencha o Codigo do produto!");
        } else {
            //variaveis
            int cod = Integer.parseInt(txtCodProd.getText());
            //---
            try {
                //seta valores no Beans
                mod.set_codProd(cod);
                //excuta a consulta
                dao.consulta(mod);
                //verifica se retornou algo
                if (dao.rs.first()) {
                    //se retorna ele exibe a mensagem 
                    JOptionPane.showMessageDialog(null, "Produto já cadastrado!");
                } else {
                    //se não existir ele libera o cadastro
                    JOptionPane.showMessageDialog(null, "Produto não encontrado, pode cadastra-lo");
                    liberar();
                }
            } catch (SQLException e) {
                //exibe o erro
                JOptionPane.showMessageDialog(null, "Problemas de conexão com a base de dados: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

