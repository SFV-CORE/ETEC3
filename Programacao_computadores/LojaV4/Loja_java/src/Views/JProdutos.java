    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        //Variaveis
        String nome = txtNomeProd.getText().trim();
        String desc = txtDesc.getText().trim();
        //-------
        //verifica se esta vazio
        if (txtCodProd.getText().isEmpty() || nome.isEmpty() || desc.isEmpty() || txtUni.getText().isEmpty() || txtUni.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor preencha todos os campos!");
        } else {
            //Variaveis
            //conversão só é feita se o textBox não estiver vazio
            double val_unit = Double.parseDouble(txtValUni.getText().trim());
            int unidade = Integer.parseInt(txtUni.getText().trim());
            int cod = Integer.parseInt(txtCodProd.getText().trim());
            //-----
            try {
                //seta as variaveis no Beans
                mod.set_codProd(cod);
                mod.set_nome_prod(nome);
                mod.set_descricao(desc);
                mod.set_valUnit(val_unit);
                mod.set_unidade(unidade);
                //executa o cadastro e guarda o retorno
                int r = dao.cadastrar(mod);
                //verifica se deu certo
                if (r == 1) {
                    //se deu certo, atualiza a tabela clientes e carrega a config inicial
                    tInicial();
                    dao.mostrarTodos((DefaultTableModel) jtProdutos.getModel());
                }
            } catch (Exception e) {
                //exibe o erro
                JOptionPane.showMessageDialog(null, "Erro: " + e);
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

