    public void limpar() {
        txtDesc.setText("");
        txtCodProd.setText("");
        txtNomeProd.setText("");
        txtValUni.setText("");
        txtUni.setText("");
        txtAtivo.setText("");
    }

    public void confAlterar() {
        txtDesc.setEnabled(true);
        txtNomeProd.setEnabled(true);
        txtValUni.setEnabled(true);
        txtUni.setEnabled(true);
        txtCodProd.setEnabled(false);

        btnAlterar.setEnabled(true);
        btnAtivo.setEnabled(true);
        btnAdd.setEnabled(false);
        btnConsultar.setEnabled(false);
        txtAtivo.setVisible(true);
        lblAtivo.setVisible(true);
    }

    public void tInicial() {
        limpar();
        txtPesquisaProd.setText("");
        txtDesc.setEnabled(false);
        txtNomeProd.setEnabled(false);
        txtValUni.setEnabled(false);
        txtUni.setEnabled(false);
        txtCodProd.setEnabled(true);

        btnAdd.setEnabled(false);
        btnAlterar.setEnabled(false);
        btnAtivo.setEnabled(false);
        btnConsultar.setEnabled(true);
    }

    public void liberar() {
        txtDesc.setEnabled(true);
        txtNomeProd.setEnabled(true);
        txtValUni.setEnabled(true);
        txtUni.setEnabled(true);
        txtCodProd.setEnabled(false);

        btnConsultar.setEnabled(false);
        btnAdd.setEnabled(true);
    }

    public void pegarValor() {
        txtCodProd.setText((String) jtProdutos.getValueAt(jtProdutos.getSelectedRow(), 0));
        txtNomeProd.setText((String) jtProdutos.getValueAt(jtProdutos.getSelectedRow(), 1));
        txtDesc.setText((String) jtProdutos.getValueAt(jtProdutos.getSelectedRow(), 2));
        txtUni.setText((String) jtProdutos.getValueAt(jtProdutos.getSelectedRow(), 4));
        txtValUni.setText((String) jtProdutos.getValueAt(jtProdutos.getSelectedRow(), 3));

        String t = (String) jtProdutos.getValueAt(jtProdutos.getSelectedRow(), 5);
        if ("1".equals(t)) {
            txtAtivo.setText("Ativado");
            btnAtivo.setText("Desativar");
        } else {
            txtAtivo.setText("Desativado");
            btnAtivo.setText("Ativar");
        }
    }

