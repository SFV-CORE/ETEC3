    private void btnAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddItemActionPerformed
        // TODO add your handling code here:
        //Verifica se está vazio
        if (txtCodProd.getText().isEmpty() || txtCodPed2.getText().isEmpty() || txtQuant.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor preencha todos os campos!");
        } else {
            //Variaveis
            int codProd = Integer.parseInt(txtCodProd.getText());
            int codPed = Integer.parseInt(txtCodPed2.getText());
            int quant = Integer.parseInt(txtQuant.getText());
            //----------
            //try catch para cadastrar produto no pedido
            try {
                //seta os valores no Beans
                modIP.set_codPed(codPed);
                modIP.set_codProd(codProd);
                modIP.set_quant(quant);
                //----
                //realiza o cadastro e guarda o retorno
                double r = daoIP.cadastrar(modIP);
                //----
                //verifica se cadastrou com sucesso
                if (r != 0) {             
                    //se sim, ele atualiza as tabelas
                    //Produto no pedido
                    daoIP.mostrarTodosItemPed(modIP, (DefaultTableModel) jTItemPedido.getModel());
                    //Pedido
                    daoP.mostrarTodos((DefaultTableModel) jtPedido.getModel());
                    //Produtos
                    daoProd.mostrarTodos((DefaultTableModel) jtProdutos.getModel());
                    //atualiza o txtValor do pedido
                    txtValPed.setText(""+r);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro: " + e);//exibe o erro
            }
        }
    }//GEN-LAST:event_btnAddItemActionPerformed

