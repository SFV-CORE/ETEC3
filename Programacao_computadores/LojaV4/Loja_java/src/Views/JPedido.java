    private void jtPedidoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtPedidoMouseClicked
        // TODO add your handling code here:
        if(btnConcluir.isVisible()){
            //se o botão concluir estiver visivel, quer dizer que a operação ainda esta em andamento e 
            //para não bugar, tem que bloquear a tabela
            JOptionPane.showMessageDialog(null, "Conclua o pedido primeiro!");
        }else{
            String[] options ={"Alterar/Excluir", "Inserir produtos", "Cancelar" };//define as opções       
                int x = JOptionPane.showOptionDialog(null, "Pedido selecionado, você deseja:", "Escolha uma das opções", 
                        JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, options, options[0]);
                //trabalha com o resultado
                switch(x){
                    case 0: //se escolher alterar/excluir, ira carregar a config de alterar, chama a função para pegar os valores da tabela
                            //detalhe, caso escolha isso, podera somente alterar ou excluir
                            confAlterar();
                            pegarValor();
                            //carrega os produtos que tem no pedido na tabela Item pedido
                            modIP.set_codPed(Integer.parseInt((String) jtPedido.getValueAt(jtPedido.getSelectedRow(), 0)));
                            daoIP.mostrarTodosItemPed(modIP, (DefaultTableModel) jTItemPedido.getModel());
  
                        break;
                    case 1: //se escolher inserir produtos, ele chama a config e tambem carrega os dados para o txt
                            confInserirProd(1);
                            //carrega os produtos que tem no pedido na tabela Item pedido
                            modIP.set_codPed(Integer.parseInt((String) jtPedido.getValueAt(jtPedido.getSelectedRow(), 0)));
                            daoIP.mostrarTodosItemPed(modIP, (DefaultTableModel) jTItemPedido.getModel());
                            //torna o borão visivel
                            btnConcluir.setVisible(true);
                        break;
                    case 2: //se escolher cancelar ele carrega a configuração inicial
                            tInicial();
                        break;
                    default: ;
                }
        }
    }//GEN-LAST:event_jtPedidoMouseClicked

    
