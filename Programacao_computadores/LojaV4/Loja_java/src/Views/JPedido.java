    private void btnConPedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConPedActionPerformed
        // TODO add your handling code here:
        //if pra ver se esta vazio
        if (txtCodPed.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor preencha todos os campos!");
        } else {
            //Variaveis
            int COD = Integer.parseInt(txtCodPed.getText());
            //--------
            //try catch para consultar
            try {
                //realiza a consulta
                modP.set_codPed(COD);
                daoP.consulta(modP);
                //----
                //verifica se encontrou algum registro
                if (daoP.rs.first())
                {     
                    //se sim ele exibe a mensagem e carrega as config iniciais
                    JOptionPane.showMessageDialog(null, "Pedido ja cadastrado!");
                    tInicial();
                } else {
                    //se não encontrar, libera para o cadastro
                    JOptionPane.showMessageDialog(null, "Pedido não encontrado, pode cadastra-lo");
                    liberar();
                }
            } catch(SQLException e) {
                JOptionPane.showMessageDialog(null, "Problemas de conexão com a base de dados: " + e.getMessage());//exibe o erro
            }
        }
    }//GEN-LAST:event_btnConPedActionPerformed

