    private void btnAltPedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAltPedActionPerformed
        // TODO add your handling code here:
        //Variaveis
        String pr_entrega = txtPrazoEntrega.getText();//prazo de entrega
        String dt_emi = txtDataDeEmissao.getText();//data de emissão
        String cnpj = txtCNPJClie.getText().trim();//cnpj do cliente
        //-------------
        //verifica se esta vazio
        if (txtCodPed.getText().isEmpty() || txtDataDeEmissao.getText().isEmpty() || txtPrazoEntrega.getText().isEmpty() || cnpj.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor preencha todos os campos!");
        } else {
            //Variaveis
            int cod_ven = daoV.pegarCodigo((String) combVend.getSelectedItem());//codigo do vendedor
            double val_total = Double.parseDouble(txtValPed.getText());
            int COD = Integer.parseInt(txtCodPed.getText());//Código do pedido
            //------
            //executa um try catch para a alteração
            try {
                //seta as variaveis no Beans
                modP.set_codPed(COD);
                modP.set_prazoEntrega(pr_entrega);
                modP.set_dt_emissao(dt_emi);
                modP.set_codVen(cod_ven);
                modP.set_cnpj(cnpj);
                modP.set_valTotal(val_total);
                //-------------
                //executa a alteração e guarda o retorno
                int r = daoP.alterar(modP);
                //---------------
                //verifica se alterou com sucesso
                if (r == 1) {
                    //se sim ele atualiza a tabela dos pedidos e carrega a configuração inicial
                    daoP.mostrarTodos((DefaultTableModel) jtPedido.getModel());
                    tInicial();
                }
                //se não o erro sera exibido pela classe
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro: " + e);//caso de errado, exibe o erro
            }
        }
    }//GEN-LAST:event_btnAltPedActionPerformed

