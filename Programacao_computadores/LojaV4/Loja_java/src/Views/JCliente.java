    public void confAlterar() {
        txtCEP.setEnabled(true);
        txtCNPJ.setEnabled(false);
        txtCidade.setEnabled(true);
        txtEndereco.setEnabled(true);
        txtNome.setEnabled(true);
        txtUF.setEnabled(true);

        btnAlterar.setEnabled(true);
        btnAtivo.setEnabled(true);
        btnAdd.setEnabled(false);
        btnConsultar.setEnabled(false);

        txtCodClie.setVisible(true);
        lblCodClie.setVisible(true);
        txtAtivo.setVisible(true);
        lblAtivo.setVisible(true);
    }

    public void limpar() {
        txtCEP.setText("");
        txtCNPJ.setText("");
        txtCidade.setText("");
        txtEndereco.setText("");
        txtNome.setText("");
        txtUF.setText("");
        txtCodClie.setText("");
    }

    public void tInicial() {
        limpar();
        btnAdd.setEnabled(false);
        btnConsultar.setEnabled(true);
        btnAlterar.setEnabled(false);
        btnAtivo.setEnabled(false);

        txtCEP.setEnabled(false);
        txtCidade.setEnabled(false);
        txtEndereco.setEnabled(false);
        txtNome.setEnabled(false);
        txtUF.setEnabled(false);
        txtCNPJ.setEnabled(true);

        txtCodClie.setVisible(false);
        lblCodClie.setVisible(false);
        txtAtivo.setVisible(false);
        lblAtivo.setVisible(false);
    }

    public void liberar() {
        txtCEP.setEnabled(true);
        txtCNPJ.setEnabled(false);
        txtCidade.setEnabled(true);
        txtEndereco.setEnabled(true);
        txtNome.setEnabled(true);
        txtUF.setEnabled(true);
        
        btnConsultar.setEnabled(false);
        btnAdd.setEnabled(true);
    }

    public void pegarValor() {
        txtCodClie.setText((String) jtCliente.getValueAt(jtCliente.getSelectedRow(), 0));
        txtNome.setText((String) jtCliente.getValueAt(jtCliente.getSelectedRow(), 1));
        txtCEP.setText((String) jtCliente.getValueAt(jtCliente.getSelectedRow(), 2));
        txtCNPJ.setText((String) jtCliente.getValueAt(jtCliente.getSelectedRow(), 3));
        txtCidade.setText((String) jtCliente.getValueAt(jtCliente.getSelectedRow(), 4));
        txtUF.setText((String) jtCliente.getValueAt(jtCliente.getSelectedRow(), 5));
        txtEndereco.setText((String) jtCliente.getValueAt(jtCliente.getSelectedRow(), 6));

        String t = (String) jtCliente.getValueAt(jtCliente.getSelectedRow(), 7);
        if ("1".equals(t)) {
            txtAtivo.setText("Ativado");
            btnAtivo.setText("Desativar");
        } else {
            txtAtivo.setText("Desativado");
            btnAtivo.setText("Ativar");
        }

    }

