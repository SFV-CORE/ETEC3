    private void btnAtivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtivoActionPerformed
        // TODO add your handling code here:
        //Verifica se esta vazio
        if (txtCodClie.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor preencha o campo codigo!");
            txtCodClie.requestFocus();
        } else {
            //variaveis
            int cod = Integer.valueOf(txtCodClie.getText());
            //---------
            if ("Desativar".equals(btnAtivo.getText())) {
                try {
                    //seta os dados e executa a desativação
                    mod.set_codClie(cod);
                    mod.setAtivo(0);
                    int r = dao.mudarAtivo(mod);
                    //verifica se deu certo
                    if (r == 1) {
                        //se deu certo, atualiza a tabela clientes e carrega a config inicial
                        tInicial();
                        dao.mostrarTodos((DefaultTableModel) jtCliente.getModel());
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Erro: " + e);//exibe o erro
                }
            } else {
                try {
                    //seta os dados e executa a ativação
                    mod.set_codClie(cod);
                    mod.setAtivo(1);
                    int r = dao.mudarAtivo(mod);
                    //verifica se deu certo
                    if (r == 1) {
                        //se deu certo, atualiza a tabela clientes e carrega a config inicial
                        tInicial();
                        dao.mostrarTodos((DefaultTableModel) jtCliente.getModel());
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Erro: " + e);//exibe o erro
                }
            }

        }

    }//GEN-LAST:event_btnAtivoActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
        //variaveis
        String CNPJ = txtCNPJ.getText();
        //---
        //verifica se esta vazio
        if (CNPJ.equals("  .   .   /    -  ")) {
            JOptionPane.showMessageDialog(null, "Por favor preencha o campo CNPJ!");
            txtCNPJ.requestFocus();
        } else {
            //try catch da consulta
            try {
                //seta valores no Beans
                mod.set_cnpj(CNPJ);
                //excuta a consulta
                dao.consulta(mod);
                //verifica se retornou algo
                if (dao.rs.first()) {
                    //se existir ele exibe a mensagem 
                    JOptionPane.showMessageDialog(null, "Cliente ja está cadastrado!");
                } else {
                    //se não existir ele libera o cadastro
                    JOptionPane.showMessageDialog(null, "Cliente não encontrado, pode cadastra-lo");
                    liberar();
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Problemas de conexão com a base de dados: " + e.getMessage());//exibe o erro
            }
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

