    private void btnAtivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtivoActionPerformed
        // TODO add your handling code here:
        //verifica se esta vazio
        if (txtCodVend.getText().isEmpty()) {
            //se estiver ele exibe a mensagem
            JOptionPane.showMessageDialog(null, "Por favor preencha o campo Código do vendedor!");
        } else {
            //Variaveis
            //conversão só é feita se o textBox não estiver vazio
            int cod = Integer.valueOf(txtCodVend.getText().trim());
            //--------
            //verifica se a operação é para ativar ou desativar o vendedor
            if ("Desabilitar".equals(btnAtivo.getText())) {
                try {
                    //seta os valores no Beans
                    mod.setCodVend(cod);
                    mod.setAtivo(0);//se for desabilitar ele seta como 0
                    //executa e guarda o retorno
                    int r = dao.mudarAtivo(mod);
                    //verifica se deu certo
                    if (r == 1) {
                        //se deu certo, atualiza a tabela clientes e carrega a config inicial
                        tInicial();
                        dao.mostrarTodos((DefaultTableModel) jtVendedor.getModel());
                    }
                } catch (Exception e) {
                    //exibe o erro
                    JOptionPane.showMessageDialog(null, "Erro: " + e);
                }
            } else {
                try {
                    //seta os valores no Beans
                    mod.setCodVend(cod);
                    mod.setAtivo(1);//se for habilitar ele seta como 1
                    //executa e guarda o retorno
                    int r = dao.mudarAtivo(mod);
                    //verifica se deu certo
                    if (r == 1) {
                        //se deu certo, atualiza a tabela clientes e carrega a config inicial
                        tInicial();
                        dao.mostrarTodos((DefaultTableModel) jtVendedor.getModel());
                    }
                } catch (Exception e) {
                    //exibe o erro
                    JOptionPane.showMessageDialog(null, "Erro: " + e);
                }
            }

        }
    }//GEN-LAST:event_btnAtivoActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
        //Variaveis
        String nome = txtNome.getText().trim();
        //-----------
        //Verifica se esta vazio
        if (nome.isEmpty()) {
            //se estiver ele exibe a mensagem
            JOptionPane.showMessageDialog(null, "Por favor preencha o campo do nome!");
        } else {
            try {
                //seta os valores no Beans
                mod.setNome(nome);
                //executa a pesquisa
                dao.consulta(mod);
                //verifica se tem registro
                if (dao.rs.first()) {
                    //se tiver registro ele bloqueia o cadastro
                    JOptionPane.showMessageDialog(null, "Vendedor já cadastrado!");
                } else {
                    //se não tiver ele libera o cadastro
                    JOptionPane.showMessageDialog(null, "Vendedor não encontrado, pode cadastra-lo");
                    liberar();
                }
            } catch (SQLException e) {
                //exibe o erro
                JOptionPane.showMessageDialog(null, "Problemas de conexão com a base de dados: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

