    public void tInicial() {
        limpar();
        txtCodVend.setEnabled(false);
        txtComissao.setEnabled(false);
        txtNome.setEnabled(true);
        txtSalFix.setEnabled(false);
        txtCodVend.setVisible(false);
        lblCodVen.setVisible(false);

        btnAdd.setEnabled(false);
        btnConsultar.setEnabled(true);
        btnAlterar.setEnabled(false);
        btnAtivo.setEnabled(false);
    }

    public void confAlterar() {
        txtCodVend.setEnabled(false);
        txtComissao.setEnabled(true);
        txtNome.setEnabled(true);
        txtSalFix.setEnabled(true);
        txtCodVend.setVisible(true);
        lblCodVen.setVisible(true);
        txtAtivo.setVisible(true);
        lblAtivo.setVisible(true);

        btnAdd.setEnabled(false);
        btnConsultar.setEnabled(false);
        btnAlterar.setEnabled(true);
        btnAtivo.setEnabled(true);
    }

    public void pegarValor() {
        txtCodVend.setText((String) jtVendedor.getValueAt(jtVendedor.getSelectedRow(), 0));
        txtNome.setText((String) jtVendedor.getValueAt(jtVendedor.getSelectedRow(), 1));
        txtSalFix.setText((String) jtVendedor.getValueAt(jtVendedor.getSelectedRow(), 2));
        txtComissao.setText((String) jtVendedor.getValueAt(jtVendedor.getSelectedRow(), 3));

        String t = (String) jtVendedor.getValueAt(jtVendedor.getSelectedRow(), 4);
        if ("1".equals(t)) {
            btnAtivo.setText("Desabilitar");
            txtAtivo.setText("Ativo");
        } else {
            btnAtivo.setText("Habilitar");
            txtAtivo.setText("Desativado");
        }

    }

    public void liberar() {
        txtComissao.setEnabled(true);
        txtSalFix.setEnabled(true);
        txtNome.setEnabled(false);

        btnAdd.setEnabled(true);
        btnConsultar.setEnabled(false);
    }

    public void limpar() {
        txtCodVend.setText("");
        txtComissao.setText("");
        txtNome.setText("");
        txtSalFix.setText("");
        txtAtivo.setText("");
    }

