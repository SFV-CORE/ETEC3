    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        //Variaveis
        String nome = txtNome.getText().trim();
        //--------
        //if para ver se esta vazio
        if (txtSalFix.getText().isEmpty() || nome.isEmpty() || txtComissao.getText().isEmpty()) {
            //se estiver ele exibe a mensagem
            JOptionPane.showMessageDialog(null, "Por favor preencha todos os campos!");
        } else {
            //Variaveis
            //conversão só é feita se o textBox não estiver vazio
            double salFixo = Double.parseDouble(txtSalFix.getText().trim());
            double comissao = Double.parseDouble(txtComissao.getText().trim());
            //--------
            try {
                //seta os valores no Beans
                mod.setNome(nome);
                mod.setSalFixo(salFixo);
                mod.setComissao(comissao);
                mod.setAtivo(1);
                //executa o cadastro e guarda o retorno
                int r = dao.cadastrar(mod);
                //verifica se cadastrou com sucesso
                if (r == 1) {
                    //se deu certo, atualiza a tabela clientes e carrega a config inicial
                    tInicial();
                    dao.mostrarTodos((DefaultTableModel) jtVendedor.getModel());
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro: " + e);//exibe o erro
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

