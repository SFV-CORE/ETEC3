    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        //Variaveis
        String nomeusu = txtNomeUsuario.getText().trim();
        String senha1 = txtSenha.getText();
        String senha2 = txtConfirmarSenha.getText();
        int adm;
        //-------------
        //if para definir se é adm
        if(txtADM.getSelectedItem() == "Comum"){
            adm = 0;
        }
        else{
            adm = 1;
        }
        //verifica se esta vazio
        if (nomeusu.isEmpty() || senha1.isEmpty() || senha2.isEmpty() || txtNmVendedor.getText().isEmpty() || txtNmUsuario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor preencha todos os campos!");
        } else 
        {
            //Variaveis, essas precisam estar depois da verificação de vazio
            int codVen = Integer.valueOf(txtNmVendedor.getText());
            int codUser = Integer.parseInt(txtNmUsuario.getText());
            //------------
            //verifica se as senhas são compativeis
            if (senha1.equals(senha2)) 
            {
                if(txtNmUsuario.getText().equals( txtNmVendedor.getText())){
                    try {
                        //seta os valores no beans
                        modU.set_codUser(codUser);
                        modU.set_nome(nomeusu);
                        modU.set_adm(adm);
                        modU.set_codVen(codVen);
                        modU.set_senha(senha2);
                        //inicia o cadastro e guarda o resultado
                        int r = daoU.cadastrar(modU);
                        //verifica o resultado
                        if(r == 1){
                            //se deu certo ele carrega a config inicial e atualiza a tabela usuarios
                            tInicial();
                            daoU.mostrarTodos((DefaultTableModel) jtUsuario.getModel());
                        }
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e.getMessage());//mostra o erro
                    }
                }
                else{
                    JOptionPane.showMessageDialog(null, "Codigo do usuario tem que ser o mesmo do vendedor!");
                }
            } else 
            {
                JOptionPane.showMessageDialog(null, "As senhas estão diferentes, tente novamente!");
            }
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

