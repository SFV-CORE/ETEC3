    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        //Verifica se esta vazio
        if (txtNmUsuario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor preencha o campo codigo do Usuario!");
            txtNmUsuario.requestFocus();
        } else {
            //variaveis
            int cod = Integer.valueOf(txtNmUsuario.getText());
            //---------
            //try catch para excluir
            try {
                //seta os dados e executa a exclusão
                modU.set_codUser(cod);
                int r = daoU.excluir(modU);
                //verifica se deu certo
                if (r == 1) {
                    //se deu certo, atualiza a tabela clientes e carrega a config inicial
                    tInicial();
                    daoU.mostrarTodos((DefaultTableModel) jtUsuario.getModel());
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro: " + e);//exibe o erro
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
        if(txtNmVendedor.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o codigo do vendedor!");
            txtNmVendedor.requestFocus();
        }else{
            int codVen = Integer.parseInt(txtNmVendedor.getText());
            try {
                //seta valores no Beans
                modU.set_codVen(codVen);
                //excuta a consulta
                daoU.consulta(modU);
                //verifica se retornou algo
                if (daoU.rs.first()) {
                    //se existir ele exibe a mensagem 
                    JOptionPane.showMessageDialog(null, "Usuario ja está cadastrado!");
                    tInicial();
                } else {
                    //se não existir ele libera o cadastro
                    JOptionPane.showMessageDialog(null, "Usuário não encontrado, pode cadastra-lo");
                    liberar();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Problemas de conexão com a base de dados: " + e.getMessage());//exibe o erro
            }
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

