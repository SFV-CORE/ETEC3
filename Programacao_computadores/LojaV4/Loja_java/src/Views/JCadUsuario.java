    private void jtUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtUsuarioMouseClicked
        // TODO add your handling code here:
        if (jtUsuario.getSelectedRow() != -1) {
            String[] options ={"Alterar/Excluir", "Cancelar" };//define as opções
                    //faz a pergunta 
                    int x = JOptionPane.showOptionDialog(null, "Usuario selecionado, você deseja:", "Escolha uma das opções", 
                            JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, options, options[0]);
                    //confere a resposta
                    switch(x){
                        case 0: //caso escolha alterar/excluir ele carrega a configuração de alteração
                                confAlterar();
                                pegarValor();
                            break;
                        case 1: //caso escolha cancelar, ele carrega a configuração da tela inicial
                                tInicial();
                            break;
                        default: ;
                    }
        }
    }//GEN-LAST:event_jtUsuarioMouseClicked

