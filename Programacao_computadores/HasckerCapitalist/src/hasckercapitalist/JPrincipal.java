    private void jDesktopPane1MouseMoved(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jDesktopPane1MouseMoved
        // TODO add your handling code here:
        lblNumDin.setText(String.valueOf(Dindin)); //atualiza o dinheiro 
        lblNumRepu.setText(String.valueOf(reputacao)); //atualiza reputacao
        
        lblCidadeAtual.setText(Cidade); //atualiza cidade
        if(cidade == 2){ // se a cidade for 2
            btnAcoes.setEnabled(true); //acoes do formAcoes2
            reputacao = 0; //zera dinheirro e reputacao por ser um novo lugar
            Dindin = 0;
        }
        
        //condição que fecha quando ocorre o "trava zap"
        if(close == 1){
             this.dispose();
        }
        
        //sequencia para a profissão
        if(reputacao >= 850 && unic == 1){ // numero de reputacao e o unic, para que a mensagem só apareca uma vez, sem o unic ela aparece infinitamente
            lblNmProf.setText("Estudante");
            JOptionPane.showMessageDialog(null,"Você agora é um simples estudante de pentest, boa sorte!","Aviso",JOptionPane.INFORMATION_MESSAGE);
            unic = 2;
        }
        else if(reputacao >= 2500 && unic == 2){
            lblNmProf.setText("Estagiário na Anatel");
            JOptionPane.showMessageDialog(null,"Você agora é um Estagiário na Anatel. Bora começar a carreira?","Aviso",JOptionPane.INFORMATION_MESSAGE);
            unic = 3;
        }
        else if(reputacao >= 5000 && unic == 3){
            lblNmProf.setText("Analista da Segurança da Informação");
            JOptionPane.showMessageDialog(null,"Você agora é um Analista da Segurança da Informação. Está no caminho certo, vamos upar?","Aviso",JOptionPane.INFORMATION_MESSAGE);
            unic = 4;
        }
        else if(reputacao >= 10000 && unic == 4){ //aqui libera o botão cidade para passar para o proximo lugar
            JOptionPane.showMessageDialog(null,"Você liberou cidades.","Aviso",JOptionPane.INFORMATION_MESSAGE);
            btnCidades.setEnabled(true);
            unic = 0;
        }
        else{
        }
    }//GEN-LAST:event_jDesktopPane1MouseMoved

